//Array Methods
// push =  adds item(s) to the end of an array
let array = ['a', 'b', 'c', 'd', 'e', 'f'];
array.push('g', 1, 'z');
console.log(array);

 //pop = removes an item from the end of an array
 array = ['a', 'b', 'c', 'd', 'e', 'f'];
 array.pop();
 console.log(array);

 //shift = removes an item from the start of an array
 array = ['a', 'b', 'c', 'd', 'e', 'f'];
 array.shift();
 console.log(array);

 //unishift= adds item(s) to the start of an array
 array = ['a', 'b', 'c', 'd', 'e', 'f'];
 array.unshift('g', 1, 'z');
 console.log(array);
